皆さん(Selfie)で空っぽ(Hollow)の棚(Shelf)を彩ってください！

## サービス名の由来

**{{ config('app.name') }}**は、『ホロウシェルフィー』と呼びます。

**Hollow** ✖ **Shelf** ✖ **Selfie** を組み合わせた造語です。

- Hollow : からっぽの
- Shelf  : 棚を
- Selfie : 自分で飾り付ける。
